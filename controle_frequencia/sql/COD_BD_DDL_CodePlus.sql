CREATE TABLE FUNCIONARIO
(
    CPF VARCHAR(11) NOT NULL,
    RG VARCHAR(15) NOT NULL,
    NOME VARCHAR(50) NOT NULL,
    NASCIMENTO DATE NOT NULL,
    NOME_PAI VARCHAR(50),
    NOME_MAE VARCHAR(50),
    ADMISSAO DATE NOT NULL,
    DESLIGAMENTO DATE,
