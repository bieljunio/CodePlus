    PRIMARY KEY(CPF),
    CONSTRAINT UK_FUNCIONARIO_RG
    UNIQUE (RG),
    CONSTRAINT UK_FUNCIONARIO_EMAIL
    UNIQUE (EMAIL),
    CONSTRAINT UK_FUNCIONARIO_RA
    UNIQUE(RA)
);
